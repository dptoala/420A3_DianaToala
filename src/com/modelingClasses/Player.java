package com.modelingClasses;

public class Player {
    //Attributes
    String name;
    String position;

    //Methods Get and Set
    public String GetName(){
        return this.name;
    }
    public void SetName(String name){
        this.name = name;
    }

    public String GetPosition(){
        return this.position;
    }
    public void setPosition(String position){
        this.position = position;
    }
}
